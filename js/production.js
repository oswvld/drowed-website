/* Animazioni Personaggio */
let animazionipersonaggioSectionBlock = document.getElementById('animazionipersonaggioSection')
let animazionipersonaggioSectionHiddenBlock = document.getElementById('animazionipersonaggioSection-hidden')

animazionipersonaggioSectionBlock.onclick = function() {
  if( animazionipersonaggioSectionHiddenBlock.style.display == 'block' )
    animazionipersonaggioSectionHiddenBlock.style.display = 'none'
  else
    animazionipersonaggioSectionHiddenBlock.style.display = 'block'
}

