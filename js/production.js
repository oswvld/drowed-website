/* Personaggio */
let personaggioSectionBlock = document.getElementById('production_personaggioSection')
let personaggioSectionHiddenBlock = document.getElementById('production_personaggioSection-hidden')

personaggioSectionBlock.onclick = function() {
  if( personaggioSectionHiddenBlock.style.display == 'block' )
    personaggioSectionHiddenBlock.style.display = 'none'
  else
    personaggioSectionHiddenBlock.style.display = 'block'
}

